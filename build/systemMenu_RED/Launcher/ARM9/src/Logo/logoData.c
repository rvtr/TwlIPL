	*(vu16 *)HW_BG_PLTT = 0xffff;										// バックドロップを「白」にする。
	
	SYSM_LoadNintendoLogo2D( (u16 *)Nin_Char_Diff_Huff, (u16 *)( HW_OBJ_VRAM + 0x0000 ), 0xf );
	
	G2_SetOBJAttr(
			(GXOamAttr *)HW_OAM,
			71,
			88,
			2,
			GX_OAM_MODE_NORMAL,
			FALSE,
			GX_OAM_EFFECT_NONE,
			GX_OAM_SHAPE_64x32,
			GX_OAM_COLORMODE_16,
			0,	// charNo.
			0,	// paletteNo.
			0
			);
